package model;

public class Book {
	/**
	 * Identifier.
	 */
	private int id;
	
	/**
	 * Name.
	 */
	private String name;

	/**
	 * Get the identifier.
	 */
	public int getId() {
		return this.id;
	}

	/**
	 * Set the identifier.
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * Get the name.
	 */
	public String getName() {
		return this.name;
	}

	/**
	 * Set the name.
	 */
	public void setName(String name) {
		this.name = name;
	}
}
